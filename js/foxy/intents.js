// Словарь для распознавания намерений (intents)
const intents = {
  greeting:     ["привет", "здравствуй", "хай", "добрый день", "хелло"],
  bye:          ["пока", "до встречи", "увидимся", "чао", "бай", "прощай"],
  thanks:       ["спасибо", "спасиб", "благодарю", "мерси", "спс", "сенкс"],
  smalltalk:    ["как дела", "что нового", "чем занимаешься", "что делаешь"],
  mood:         ["норм", "всё хорошо", "все ок", "заебись", "отлично", "мда"],
  softWarning:  ["дура", "тупая", "идиотка"],
  fallback:     ["че", "чо", "что ты", "непонятно", "шта", "чё"],
  help:         ["помоги", "подскажи", "что ты умеешь", "расскажи", "зачем ты нужна"],
  about:        ["о себе", "кто ты", "твоя цель", "что ты такое", "что ты умеешь"],
  showServices: ["услуги", "прайс", "маникюр", "покрытие", "цены", "расскажи про услуги"],
  booking: [
    "записаться", "запиши", "запиши меня", "запиши пожалуйста", "хочу запись", 
    "можно записаться", "можно к тебе", "хочу к тебе", "можно попасть", "где запись"
  ],
  design:       ["дизайн", "пример", "варианты", "инспо", "покажи идеи", "что модно"]
};

export function matchIntent(message) {
  for (const intent in intents) {
    const keywords = intents[intent];
    if (keywords.some(kw => message.includes(kw))) return intent;
  }
  return null;
}
