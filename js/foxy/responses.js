export function matchService(text) {
  const norm = text.toLowerCase().trim();

  for (const key in services) {
    if (norm === key) return { name: key, exact: true };
  }
  for (const key in services) {
    if (norm.includes(key)) return { name: key, exact: false };
  }
  return null;
}

