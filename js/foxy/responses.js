  for (const key of Object.keys(services)) {
    if (input === key) {
      return { name: key, exact: true };
    }
  }
  // 2) –ß–∞—Å—Ç–∏—á–Ω–æ–µ –ø–æ –ø–µ—Ä–≤–æ–º—É —Å–ª–æ–≤—É
