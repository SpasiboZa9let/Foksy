export function matchService(message) {
  const normalized = message.toLowerCase().trim();

  for (const name in services) {
    if (normalized === name) return { name, exact: true };

    const aliases = serviceAliases[name] || [];
    if (aliases.some(alt => normalized.includes(alt)))
      return { name, exact: false };
  }

  return null;
}

